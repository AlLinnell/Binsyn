      SUBROUTINE RCHERD(I,ITH,K) 
C     
C     THIS SRT CALCULATES REMAINING GEOMETRIC QUANTITIES FOLLOWING RCHER
C     WHEN THERE IS NON-SYNCHRONOUS ROTATION 
C     THE PARAMETER I DESIGNATES THE COMPONENT IN QUESTION AND K  
C     SEQUENTIALLY NUMBERS POINTS ON THE SURFACE OF THAT COMPONENT
C     L DESIGNATES THE THETA VALUE    
      INCLUDE 'COMA.FOR'
C     The original derivatives, POPR(I,K) and POPT, were for the
C     component in synchronous rotation. Must now use the derivatives
C     for the non-synchronously distorted component.
C     Calculate the radial gravity term due to 1/r^2
	TERM1=-1.D0/VR(I,ITH,K)**2
C     Calculate the radial gravity term due to centripital acceleration
	TERM2=FV(I)**2*(1.D0+Q)*VR(I,ITH,K)*
     $		(1.D0-ANU(I,ITH,1)**2)
	BKT1P=(1.D0-2.D0*ALM(I,ITH,K)*VR(I,ITH,K)+VR(I,ITH,K)**2)**1.5D0
	TERM3=Q*((ALM(I,ITH,K)-VR(I,ITH,K))/BKT1P-ALM(I,ITH,K))
	POPR(I,ITH,K)=TERM1+TERM2+TERM3
	POPR(I,ITH,K)=-DABS(POPR(I,ITH,K))
	SNP=DSIN(PH(I,ITH,K))
	CSP=DCOS(PH(I,ITH,K))
	POPL(I,ITH,K)=Q*(VR(I,ITH,K)/BKT1P-VR(I,ITH,K))
	POPN(I,ITH,K)=-(1.D0+Q)*FV(I)**2*ANU(I,ITH,K)*VR(I,ITH,K)**2
	PLPT=ANU(I,ITH,1)*CSP
	PMPT=ANU(I,ITH,1)*SNP
	PNPT=-SNT(I,ITH)
	PLPP=PNPT*SNP
	PMPP=ALM(I,ITH,K)*SNT(I,ITH)
	POPT=POPL(I,ITH,K)*PLPT+POPN(I,ITH,K)*PNPT
	POPP=POPL(I,ITH,K)*PLPP
	IF(DABS(SNT(I,ITH)).GT.1.D-11) THEN
		G(I,ITH,K)=-DSQRT(POPR(I,ITH,K)**2+POPT**2/VR(I,ITH,K)**2+
     $		1.D0/VR(I,ITH,K)**2/SNT(I,ITH)**2*POPL(I,ITH,K)**2)
		POPX=ALM(I,ITH,K)*POPR(I,ITH,K)+PLPT/VR(I,ITH,K)*POPT+
     $		SNP**2/VR(I,ITH,K)*POPL(I,ITH,K)
		POPY=AMU(I,ITH,K)*POPR(I,ITH,K)+PMPT/VR(I,ITH,K)*POPT-
     $		CSP*SNP/VR(I,ITH,K)*POPL(I,ITH,K)
		POPZ=ANU(I,ITH,K)*POPR(I,ITH,K)+PNPT/VR(I,ITH,K)*POPT
	ELSE
		G(I,ITH,K)=-DSQRT(POPR(I,ITH,K)**2+POPT**2/VR(I,ITH,K)**2+
     $		1.D0/VR(I,ITH,K)**2*SNP**2*POPL(I,ITH,K)**2)
		POPX=ALM(I,ITH,K)*POPR(I,ITH,K)+PLPT/VR(I,ITH,K)*POPT+
     $		SNP**2/VR(I,ITH,K)*POPL(I,ITH,K)
		POPY=AMU(I,ITH,K)*POPR(I,ITH,K)+PMPT/VR(I,ITH,K)*POPT-
     $		SNP*CSP/VR(I,ITH,K)*POPL(I,ITH,K)
		POPZ=ANU(I,ITH,K)*POPR(I,ITH,K)+PNPT/VR(I,ITH,K)*POPT		
	END IF
      ZL(I,ITH,K)=POPX/G(I,ITH,K)      
      ZM(I,ITH,K)=POPY/G(I,ITH,K)      
      ZN(I,ITH,K)=POPZ/G(I,ITH,K)
      CSB(I,ITH,K)=ALM(I,ITH,K)*ZL(I,ITH,K)+AMU(I,ITH,K)*ZM(I,ITH,K)+
     $ANU(I,ITH,1)*ZN(I,ITH,K)
      IF (CSB(I,ITH,K).GT.1.D0) CSB(I,ITH,K)=1.D0
      RETURN     
      END 

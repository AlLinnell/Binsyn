      SUBROUTINE AWTO(NRM,NSG,NRING,RA,RB,HV,A,
     $ITHU,ITHL,ISTAR,NTYPE,VC,HVBL,THBL,NOUT)
C     SRT to write output for Program ACPGA
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	INCLUDE 'ACOMA.FOR'
	DIMENSION VC(MRM)
  100 FORMAT (5E22.13)
  101 FORMAT (10I5)
      WRITE (NOUT,101) NRM,NSG,NRING,ITHU,ITHL,ISTAR,NTYPE
      WRITE (NOUT,100) RA,RB,HV,A,(THR(I),I=1,NRM)
      WRITE (NOUT,100) (SNTR(I),I=1,NRM)
	WRITE (NOUT,100) (VC(I),I=1,NRM)
	WRITE (NOUT,100) (RMR(I),I=1,NRM)
      WRITE (NOUT,100) (GR(I),I=1,NRM)
      WRITE (NOUT,100) (PHIR(I),I=1,NSG)
      DO I=1,NRM
      WRITE (NOUT,100) (VXAR(I,J),VYAR(I,J),VZAR(I,J),J=1,NSG)
      WRITE (NOUT,100) (ALMAR(I,J),AMUAR(I,J),ANUAR(I,J),J=1,NSG)
      WRITE (NOUT,100) (ZLAR(I,J),ZMAR(I,J),ZNAR(I,J),J=1,NSG)
      WRITE (NOUT,100) (CSBR(I,J),J=1,NSG)
      END DO
      WRITE (NOUT,100) (RGRD(I),I=1,NRING)
      WRITE (NOUT,100) (FVF(I),I=1,NRING)
      WRITE (NOUT,100) (GF(I),I=1,NRING)
      WRITE (NOUT,100) (GPER(I),I=1,NRING)
      WRITE (NOUT,100) (SNTF(I),I=1,NRING)
      WRITE (NOUT,100) (ZLAF(J),ZMAF(J),ZNAF(J),J=1,NSG)
      WRITE (NOUT,100) (ALMAF(J),AMUAF(J),ANUAF(J),J=1,NSG)
      DO I=1,NRING
      WRITE (NOUT,100) (VXAF(I,J),VYAF(I,J),VZAF(I,J),J=1,NSG)
      WRITE (NOUT,100) (CSBF(I,J),J=1,NSG)
      END DO
	WRITE (NOUT,100) HVBL,THBL
      RETURN
      END

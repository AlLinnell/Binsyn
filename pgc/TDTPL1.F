      SUBROUTINE TDTPL1(J,K,L,GRV,TMP,WL,UV)
      INCLUDE 'COMA.FOR'      
      CALL LNTER(RTMP(K),U1R(J,K,L),RTMP(K+1),U1R(J,K+1,L),TMP,U1V)
      CALL LNTER(RTMP(K),U1R(J+1,K,L),RTMP(K+1),U1R(J+1,K+1,L),TMP,U2V)
      CALL LNTER(RTMP(K),U1R(J,K,L+1),RTMP(K+1),U1R(J,K+1,L+1),TMP,U3V)
      CALL LNTER(RTMP(K),U1R(J+1,K,L+1),RTMP(K+1),U1R(J+1,K+1,L+1),TMP,
     $U4V) 
      CALL LNTER(RWL(L),U2V,RWL(L+1),U4V,WL,U6V)
      CALL LNTER(RWL(L),U1V,RWL(L+1),U3V,WL,U5V)
      CALL LNTER(RGRAV(J),U5V,RGRAV(J+1),U6V,GRV,UV)
      RETURN     
      END

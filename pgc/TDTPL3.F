      SUBROUTINE TDTPL3(J,K,L,GRV,TMP,WL,B) 
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INCLUDE 'COMDA.FOR'
      CALL LNTER(RTMP(K),RFLX(J,K,L),RTMP(K+1),RFLX(J,K+1,L),TMP,B1)
      CALL LNTER(RTMP(K),RFLX(J+1,K,L),RTMP(K+1),RFLX(J+1,K+1,L),TMP,
     $B2)
      CALL LNTER(RTMP(K),RFLX(J,K,L+1),RTMP(K+1),RFLX(J,K+1,L+1),TMP,
     $B3)
      CALL LNTER(RTMP(K),RFLX(J+1,K,L+1),RTMP(K+1),RFLX(J+1,K+1,L+1),
     $TMP,B4)
      CALL LNTER(RWL(L),B1,RWL(L+1),B3,WL,B5)
      CALL LNTER(RWL(L),B2,RWL(L+1),B4,WL,B6)
      CALL LNTER(RGRAV(J),B5,RGRAV(J+1),B6,GRV,B)   
      RETURN     
      END 

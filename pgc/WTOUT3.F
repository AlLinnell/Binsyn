      SUBROUTINE WTOUT3(I,NTH,NWL,WL,UC1,UC2,A,B,C,D,E,F,GV, 
     $H,BA,BB,UA,UB,KU)    
	INCLUDE 'COMA.FOR'
      DIMENSION WL(MWL)  
  106 FORMAT (10I5)     
  108 FORMAT (5D22.13)  
      WRITE (KU,106) I,NTH,NWL
      WRITE (KU,108) UC1,UC2,A,B,C,D,E,F,GV,H,BA,BB,UA,UB    
      WRITE (KU,108) (WL(K),K=1,NWL)
	DO ITH=1,NTH
	N=NPH(I,ITH)
      WRITE (KU,108) (TMPR(I,ITH,K),K=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      WRITE (KU,108)(TMPR(3-I,ITH,K),K=1,N)      
	END DO
	DO ITH=1,NTH
	N=NPH(I,ITH)
      WRITE (KU,108) (FIN(I,ITH,K),K=1,N) 
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      WRITE (KU,108) (FIN(3-I,ITH,K),K=1,N)      
	END DO
	DO ITH=1,NTH
	N=NPH(I,ITH)
      WRITE (KU,108) (S(I,ITH,K),K=1,N)   
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      WRITE (KU,108) (S(3-I,ITH,K),K=1,N) 
	END DO
      RETURN     
      END 

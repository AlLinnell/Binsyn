      SUBROUTINE ACRD2(NWL,NSG,NRM,NRING,IRCT,WLV,
     $TMPRF,TMPRFA,SMV,TMPRR,RMIFLX,IN)
C     SRT to input data written by Program ACPGC
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	INCLUDE 'ACOMA.FOR'
      DIMENSION TMPRF(MRING,MSG),TMPRR(MRM,MSG),
     $TMPRFA(MRING,MSG),SMV(MRING,MSG),RMIFLX(MRM,MSG)
  100 FORMAT (4I5)
  101 FORMAT (5E22.13)
      READ (IN,100) NWL,NSG,NRM,NRING,IRCT
      READ(IN,101) WLV
      DO I=1,NRING
      READ (IN,101) (U1F(I,J),J=1,NSG)
      READ (IN,101) (U2F(I,J),J=1,NSG)
      READ (IN,101) (BDIF(I,J),J=1,NSG)
      READ (IN,101) (TMPRF(I,J),J=1,NSG)
      READ (IN,101) (TMPRFA(I,J),J=1,NSG)
	READ (IN,101) (SMV(I,J),J=1,NSG)
      END DO
      DO I=1,NRM
      READ (IN,101) (U1RM(I,J),J=1,NSG)
      READ (IN,101) (U2RM(I,J),J=1,NSG)
      READ (IN,101) (BDIR(I,J),J=1,NSG)
      READ (IN,101) (TMPRR(I,J),J=1,NSG)
	READ (IN,101) (RMIFLX(I,J),J=1,NSG)
      END DO
      RETURN
      END

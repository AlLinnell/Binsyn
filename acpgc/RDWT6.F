      SUBROUTINE RDWT6(I,IWL,NTH,KU)
	INCLUDE 'COMA.FOR'
  108 FORMAT (5D22.13)  
  100 FORMAT (7F18.13)   
  101 FORMAT (3I5)      
      READ (KU,101,END=99) I,IWL,NTH
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (KU,100) (U1(I,ITH,K),K=1,N)
	END DO  
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (KU,100) (U2(I,ITH,K),K=1,N)  
	END DO  
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (KU,100) (U1(3-I,ITH,K),K=1,N)
	END DO  
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (KU,100) (U2(3-I,ITH,K),K=1,N)
	END DO  
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (KU,108) (BDI(I,ITH,K),K=1,N) 
	END DO  
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (KU,108) (BDI(3-I,ITH,K),K=1,N)
	END DO  
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (KU,108) (BDJ(I,ITH,K),K=1,N)
	END DO  
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (KU,108) (BDJ(3-I,ITH,K),K=1,N)      
	END DO  
      RETURN     
   99 CALL PMDSTOP
      END 

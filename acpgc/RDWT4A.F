      SUBROUTINE RDWT4A(NGR,NTMP,NRF,U1Q,U2Q,NU) 
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION U1Q(10,22,14),U2Q(10,22,14)  
  100 FORMAT (1X,14F7.3)   
      DO 11 J=1,NGR     
      DO 10 K=1,NTMP    
      READ (NU,100,END=99) (U1Q(J,K,L),L=1,NRF)     
      READ (NU,100,END=99) (U2Q(J,K,L),L=1,NRF)     
   10 CONTINUE   
   11 CONTINUE   
      RETURN     
   99 CALL PMDSTOP
      END 

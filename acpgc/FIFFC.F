      SUBROUTINE FIFFC(TMPI,RDV,VRC,VMU,BALB,VINORM,TMP,SM,A,FLX1,FLX2,
     $NOUT1)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION ORDIN(25)
      DATA SGMA/5.6696D-5/
      FRACT=VRC/RDV
      COEF=2.D0*((FRACT)**3)*VINORM
      VLL=FRACT
      VUL=1.D0
      DLT=(VUL-VLL)/24.D0
      DO J=1,25
         ABSC=(J-1)*DLT+VLL
         A=ABSC-FRACT
         D=1.D0+FRACT**2-2.D0*FRACT*ABSC
         B=(1.D0-VMU+VMU*A/D**0.5D0)
         C=A/D**2
         ORDIN(J)=B*C*(1.D0-ABSC**2)
      END DO
      CALL ITF(ORDIN,1,25,DLT,YM1,YNP1,0,ER,SM,25,KRT)
      SM=SM*COEF
      FLX1=SGMA*TMPI**4
      FLX2=BALB*SM
      A=FLX1+FLX2
      TMP=(A/SGMA)**0.25D0
      RETURN
      END SUBROUTINE

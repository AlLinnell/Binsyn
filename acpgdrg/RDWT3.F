      SUBROUTINE RDWT3(I,NTH,NWL,WL,UC1,UC2,A,B,C,D,E,F,  
     $GV,H,BA,BB,UA,UB,KU)    
	INCLUDE 'COMA.FOR'
      DIMENSION WL(MWL)  
  106 FORMAT (10I5)     
  108 FORMAT (5D22.13)  
      READ (KU,106,END=99) I,NTH,NWL
      READ (KU,108) UC1,UC2,A,B,C,D,E,F,GV,H,BA,BB,UA,UB    
      READ (KU,108) (WL(K),K=1,NWL)
	DO ITH=1,NTH
	CALL IDX(I,ITH,KST)
	N=NPH(I,ITH)
	KND=KST+N-1   
      READ (KU,108) (TMPR(I,K),K=KST,KND)
	END DO
	DO ITH=1,NTH
	CALL IDX(3-I,ITH,KST)
	N=NPH(3-I,ITH)
	KND=KST+N-1   
      READ (KU,108)(TMPR(3-I,K),K=KST,KND)      
	END DO
	DO ITH=1,NTH
	CALL IDX(I,ITH,KST)
	N=NPH(I,ITH)
	KND=KST+N-1   
      READ (KU,108) (FIN(I,K),K=KST,KND) 
	END DO
	DO ITH=1,NTH
	CALL IDX(3-I,ITH,KST)
	N=NPH(3-I,ITH)
	KND=KST+N-1   
      READ (KU,108) (FIN(3-I,K),K=KST,KND)      
	END DO
	DO ITH=1,NTH
	CALL IDX(I,ITH,KST)
	N=NPH(I,ITH)
	KND=KST+N-1   
      READ (KU,108) (S(I,K),K=KST,KND)   
	END DO
	DO ITH=1,NTH
	CALL IDX(3-I,ITH,KST)
	N=NPH(3-I,ITH)
	KND=KST+N-1   
      READ (KU,108) (S(3-I,K),K=KST,KND) 
	END DO
      RETURN     
   99 CALL PMDSTOP
      END 

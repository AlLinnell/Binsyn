      SUBROUTINE RTFDA(VMN,VMX,X1,X2,OMVMU,VLMU,VLG2,ERP,KLM1)     
C     SRT TO LOCATE ROOT OF FUNCTION ROOTFN      
C     THE ROOT IS BETWEEN THE INDEPENDENT VARIABLE VALUES VMN AND VMX 
C     THE ROOT MUST BE LOCATED TO WITHIN AN ACCURACY LIMIT ERP    
C     UP TO KLM1 ITERATIONS ARE ALLOWED TO FIND THE ROOT      
C     ON EXIT THE VALUE OF THE INDEPENDENT VARIABLE FOR THE ROOT IS IN VLG2
C     X1,X2,OMVMU, and VLMU ARE PARAMETERS.
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	PARAMETER (MFR    =    4)
      DIMENSION A(MFR),V(MFR)      
      IF (VMN.GT.VMX) GO TO 200    
      A(1)=VMN     
      A(3)=VMX     
      GO TO 210  
  200 A(1)=VMX     
      A(3)=VMN   
  210 CALL ROOTFN(A(1),X1,X2,OMVMU,VLMU,V(1)) 
      IF (DABS(V(1)).LT.ERP) GO TO 100 
      CALL ROOTFN(A(3),X1,X2,OMVMU,VLMU,V(3)) 
      IF (DABS(V(3)).LT.ERP) GO TO 110 
      DO 40 I=1,KLM1
      A(2)=0.5D0*(A(1)+A(3))     
      CALL ROOTFN(A(2),X1,X2,OMVMU,VLMU,V(2)) 
      IF (DABS(V(2)).LT.ERP) GO TO 120 
      CALL LAGIN(0.D0,VLG2,V,A,2,3,NR,0.D0)      
      IF (NR.LT.3) CALL PMDSTOP
      IF ((VLG2.GT.A(1)).AND.(VLG2.LT.A(3))) GO TO 77   
      DO 70 J=1,2
      IF ((V(J).LT.0.D0).AND.(V(J+1).GT.0.D0)) GO TO 71      
      IF ((V(J).GT.0.D0).AND.(V(J+1).LT.0.D0)) GO TO 71      
   70 CONTINUE   
      CALL PMDSTOP      
   71 CALL LNTER(V(J),A(J),V(J+1),A(J+1),0.D0,VLG2)    
   77 CALL ROOTFN(VLG2,X1,X2,OMVMU,VLMU,VLG2A)    
      IF (DABS(VLG2A).LT.ERP) GO TO 99   
      A(4)=A(3)+10000000.D0
      CALL SRTA(VLG2,VLG2A,A,V,1,4)
      DO 81 K1=1,3      
      IF ((V(K1).LT.0.D0).AND.(V(K1+1).GT.0.D0)) GO TO 85    
      IF ((V(K1).GT.0.D0).AND.(V(K1+1).LT.0.D0)) GO TO 85    
   81 CONTINUE   
      CALL PMDSTOP      
   85 A(1)=A(K1) 
      A(3)=A(K1+1)      
      V(1)=V(K1) 
      V(3)=V(K1+1)      
   40 CONTINUE   
      CALL PMDSTOP      
  100 VLG2=A(1)    
   99 RETURN     
  110 VLG2=A(3)    
      GO TO 99   
  120 VLG2=A(2)    
      GO TO 99   
      END 

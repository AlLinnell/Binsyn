      SUBROUTINE LNTER(X1,Y1,X2,Y2,XA,YA)    
C     SRT FOR LINEAR INTERPOLATION BETWEEN TWO POINTS      
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	DATA ERLT/1.D-13/
      IF (DABS(Y1-Y2).LE.DABS(ERLT*Y1)) GO TO 10   
      IF (DABS(XA-X1).LE.DABS(ERLT*XA)) GO TO 10   
      IF (DABS(XA-X2).LE.DABS(ERLT*XA)) GO TO 11   
      YA=(Y2-Y1)/(X2-X1)*(XA-X1)+Y1   
   20 RETURN     
   10 YA=Y1      
      GO TO 20   
   11 YA=Y2      
      GO TO 20   
      END 

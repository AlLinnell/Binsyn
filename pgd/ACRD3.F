      SUBROUTINE ACRD3(NSG,NRM,NRING,CSGR,CSGF,PXR,PYR,PZR,
     $PXF,PYF,PZF,XHZ,YHZ,PRFH,PRSH,PRRH,XRMC,YRMC,RBP,STX,STY,STZ,
     $STVS,RXHZL,RYHZL,RXHZG,RYHZG,PRVFG,PRVFL,XUL,XLL,YUL,YLL,RVLP,
     $ANG,RD,AGLW,VXP,VYP,IFC,IRC,IFCS,IRCS,IGAIN,I,IEC,IFLAG,NTH,IN)
C     Srt to input data written by Program ACPGB
	INCLUDE 'COMA.FOR'
	INCLUDE 'ACOMA.FOR'
      DIMENSION CSGR(MRM,MSG),CSGF(MRING,MSG),PXR(MRM,MSG),PYR(MRM,MSG),
     $PZR(MRM,MSG),PXF(MRING,MSG),PYF(MRING,MSG),PZF(MRING,MSG),
     $PRRH(MRM,MSG),
     $PRFH(MRING,MSG),XHZ(MSG),YHZ(MSG),RVLP(MSG),ANG(MSG),RD(MSG),
     $AGLW(MSG)
C
      DIMENSION STX(MTW,MTHM,MPHM),STY(MTW,MTHM,MPHM),
     $STZ(MTW,MTHM,MPHM),
     $STVS(MTW,MTHM,MPHM),
     $VXP(MTW,MPHM,MTW),VYP(MTW,MPHM,MTW),PRSH(MTW,MPHM,MTW)
C
      DIMENSION RXHZL(MRM),RYHZL(MRM),RXHZG(MRM),RYHZG(MRM),
     $PRVFG(MRM),PRVFL(MRM)
  100 FORMAT (16I5)
  101 FORMAT (5E22.13)
      READ (IN,100) NSG,NRM,NRING,IFC,IRC,IFCS,IRCS,IGAIN,I,IEC,IFLAG,
     $NRMHZ
      READ (IN,101) XUL,XLL,YUL,YLL
      IF (NSG.GT.0) READ (IN,101) (RVLP(J),J=1,NSG)
      IF (NSG.GT.0) READ (IN,101) (ANG(J),J=1,NSG)
      IF (NSG.GT.0) READ (IN,101) (RD(J),J=1,NSG)
      IF (NSG.GT.0) READ (IN,101) (AGLW(J),J=1,NSG)
      IF (NRM.GT.0) THEN
         DO K=1,NRM
         READ (IN,101) (CSGR(K,J),J=1,NSG)
         END DO
      END IF
      IF (NRING.GT.0) THEN
         DO K=1,NRING
         READ (IN,101) (CSGF(K,J),J=1,NSG)
         END DO
      END IF
      IF (NRM.GT.0) THEN
         DO K=1,NRM
         READ (IN,101) (PXR(K,J),J=1,NSG)
         READ (IN,101) (PYR(K,J),J=1,NSG)
         READ (IN,101) (PZR(K,J),J=1,NSG)
         END DO
      END IF
      IF (NRING.GT.0) THEN
         DO K=1,NRING
         READ (IN,101) (PXF(K,J),J=1,NSG)
         READ (IN,101) (PYF(K,J),J=1,NSG)
         READ (IN,101) (PZF(K,J),J=1,NSG)
         END DO
      END IF
      IF (NSG.GT.0) READ (IN,101) (XHZ(J),J=1,NSG)
      IF (NSG.GT.0) READ (IN,101) (YHZ(J),J=1,NSG)
      READ (IN,101) (PRSH(I,J,1),J=1,IFCS)
      READ (IN,101) (VXP(I,J,1),J=1,IFCS)
      READ (IN,101) (VYP(I,J,1),J=1,IFCS)
      READ (IN,101) (PRSH(I,J,2),J=1,IRCS)
      READ (IN,101) (VXP(I,J,2),J=1,IRCS)
      READ (IN,101) (VYP(I,J,2),J=1,IRCS)
      READ (IN,101) (PRSH(3-I,J,1),J=1,IFC)
      READ (IN,101) (VXP(3-I,J,1),J=1,IFC)
      READ (IN,101) (VYP(3-I,J,1),J=1,IFC)
      READ (IN,101) (PRSH(3-I,J,2),J=1,IRC)
      READ (IN,101) (VXP(3-I,J,2),J=1,IRC)
      READ (IN,101) (VYP(3-I,J,2),J=1,IRC)
      IF (NRING.GT.0) THEN
         DO K=1,NRING
         READ (IN,101) (PRFH(K,J),J=1,NSG)
         END DO
      END IF
      IF (NRM.GT.0) THEN
         DO K=1,NRM
         READ (IN,101) (PRRH(K,J),J=1,NSG)
         END DO
      END IF
      READ (IN,101) XRMC,YRMC,RBP
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (IN,101) (STX(I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (IN,101) (STX(3-I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (IN,101) (STY(I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (IN,101) (STY(3-I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (IN,101) (STZ(I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (IN,101) (STZ(3-I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(I,ITH)
      READ (IN,101) (STVS(I,ITH,J),J=1,N)
	END DO
	DO ITH=1,NTH
	N=NPH(3-I,ITH)
      READ (IN,101) (STVS(3-I,ITH,J),J=1,N)
	END DO
      IF (NRMHZ.GT.1) READ (IN,101) (RXHZL(J),J=1,NRMHZ)
      IF (NRMHZ.GT.1) READ (IN,101) (RYHZL(J),J=1,NRMHZ)
      IF (NRMHZ.GT.1) READ (IN,101) (RXHZG(J),J=1,NRMHZ)
      IF (NRMHZ.GT.1) READ (IN,101) (RYHZG(J),J=1,NRMHZ)
      IF (NRMHZ.GT.1) READ (IN,101) (PRVFG(J),J=1,NRMHZ)
      IF (NRMHZ.GT.1) READ (IN,101) (PRVFL(J),J=1,NRMHZ)
      RETURN
      END

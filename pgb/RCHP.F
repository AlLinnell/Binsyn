      SUBROUTINE RCHP(I,ITH,K,R,VOMP)   
C     
C     THIS SRT CALCULATES THE DERIVATIVE OF THE POTENTIAL FOR SPECIFIED  
C     DISTANCE FROM THE ORIGIN ALONG A LINE OF GIVEN DIRECTION COSINES   
C     IF THE DISTANCE IS LESS THAN THE XMNDISTANCE THE NONSYNCHRONOUS    
C     ROTATION PARAMETER FV(I) IS USED
C     IF THE DISTANCE IS GREATER THAN XMNSYNCHRONOUS ROTATION IS ADOPTED 
C     THE R-DERIVATIVE IS STORED IN THE ARRAY POPR  
C     L DESIGNATES THE THETA VALUE    
      INCLUDE 'COMA.FOR'
      BKT1(I)=1.D0-2.D0*ALM(I,ITH,K)*R+R*R  
      VOMP=-1.D0/R/R+Q*((ALM(I,ITH,K)-R)/BKT1(I)**1.5D0-ALM(I,ITH,K))
     $+(1.D0+Q)*(1.D0-ANU(I,ITH,K)*ANU(I,ITH,K))*R
    3 POPR(I,ITH,K)=VOMP    
    4 RETURN     
      END 

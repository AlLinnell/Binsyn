      SUBROUTINE PARAB(AR3,AR2)   
C     SUBROUTINE TO PASS A PARABOLA THROUGH THREE POINTS   
C     AT ENTRY,AR3 CONTAINS THE ABSCISSAE,AR2 THE ORDINATES
C     AT EXIT,AR2 CONTAINS THE PARAMETERS OF THE PARABOLA  
C     THE PARABOLA IS GIVEN BY Y=C+B*X+A*X*X
C     AT EXIT,AR2(1)=C,AR2(2)=B,AR2(3)=A
C     THE ABSCISSA OF THE PARABOLA EXTREMUM IS
C     AT X=-AR2(2)/2.0/AR2(3)
C     THE ORDINATE TO THE EXTREMUM IS
C     AT Y=AR2(1)+AR2(2)*X,WHERE X IS THE EXTREMUM ABSCISSA
	USE COMA
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION AR2(MTR),AR3(MTR)
C     Direct solution
      D1=AR3(1)-AR3(3)
	D2=AR3(2)-AR3(3)
	D3=AR2(1)-AR2(3)
	D4=AR2(2)-AR2(3)
	D5=AR3(1)**2-AR3(3)**2
	D6=AR3(2)**2-AR3(3)**2
	VNUM=D4*D1-D3*D2
	VDEN=D6*D1-D5*D2
	A=VNUM/VDEN
	D7=D3*D2-A*D5*D2
	B=D7/D2/D1
	C=AR2(1)-B*AR3(1)-A*AR3(1)**2
	AR2(1)=C
	AR2(2)=B
	AR2(3)=A	  
      RETURN     
      END 

      SUBROUTINE WTMTXA(A,IRANK,NTO)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION A(50,50)
  100 FORMAT (' ')
      KDOWN=IRANK
      LST2=1
      DO KDN=1,9
      IF (KDOWN.GE.6) THEN
         KDNLG=6
      ELSE
         KDNLG=KDOWN
      END IF
      KCRLG=IRANK
      LST1=1
      DO KCR=1,9
      IF (KCRLG.GE.6) THEN
         KCRRK=6
      ELSE
         KCRRK=KCRLG
      END IF
      IF ((KCRLG.GE.6).AND.(KDOWN.GE.6)) THEN
         CALL WTMTX(A,LST1,LST1+KCRRK-1,LST2,LST2+KDNLG-1,NTO)
         WRITE (NTO,100)
         GO TO 1
      END IF
      IF ((KCRLG.GE.1).AND.(KDOWN.GE.6)) THEN
         CALL WTMTX(A,LST1,LST1+KCRRK-1,LST2,LST2+KDNLG-1,NTO)
         WRITE (NTO,100)
         GO TO 1
      END IF
      IF ((KCRLG.GE.6).AND.(KDOWN.GE.1)) THEN
         CALL WTMTX(A,LST1,LST1+KCRRK-1,LST2,LST2+KDNLG-1,NTO)
         WRITE (NTO,100)
         GO TO 1
      END IF
      IF ((KCRLG.GE.1).AND.(KDOWN.GE.1)) THEN
         CALL WTMTX(A,LST1,LST1+KCRRK-1,LST2,LST2+KDNLG-1,NTO)
         WRITE (NTO,100)
      END IF
    1 KCRLG=KCRLG-KCRRK
      LST1=LST1+KCRRK
      END DO
      KDOWN=KDOWN-KDNLG
      LST2=LST2+KDNLG
      END DO
      RETURN
      END

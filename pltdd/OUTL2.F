      SUBROUTINE OUTL2(XG,YG,IEC,ISEQ,ISV,I,IRC,LCT)
	INCLUDE 'COMA.FOR'
	DIMENSION AR2(3),AR3(3),LCT(16)
	CSPJ=1.D0
	SNPJ=0.D0
  100 DO 110 J=1,IRC-1
      AR3(1)=ALP(J,2)
	AR3(2)=ALP(J+1,2)
	AR2(1)=RS(J,2)
	AR2(2)=RS(J+1,2)
	CALL STLINE(AR3,AR2,0,IER)
	IF (IER.EQ.129) CALL PMDSTOP
      AGL1=ALP(J,2)
	RG=(ALP(J+1,2)-ALP(J,2))/16.D0
	JLM=16
	JKL=0
	IF (K.GT.0) GO TO 101
	JLM=17
	JKL=1
  101 DO 120 JJ=1,JLM
      CFF=JJ-JKL
	ANGLE=AGL1+CFF*RG
	VRHO=AR2(1)+AR2(2)*ANGLE
      A=VRHO*DCOS(ANGLE)
      B=VRHO*DSIN(ANGLE)
      YP=A
      IF ((I.NE.ISV).AND.(ISEQ.LT.3)) YP=-YP
      IF ((I.EQ.ISV).AND.(ISEQ.GT.2)) YP=-YP
      XP=B
      IF (I.EQ.ISV) GO TO 102  
      IF (ISV.NE.IEC) GO TO 103
      YP=YP-YG   
      XP=XP-XG
	GO TO 102
  103 YP=YP+YG
      XP=XP+XG
  102 CONTINUE
      K=K+1
	XR(K)=XP
	YR(K)=YP
	IF (LCT(15).EQ.1) GO TO 110
	YR(K)=XP*CSPJ-YP*SNPJ
	XR(K)=XP*SNPJ+YP*CSPJ
  120 CONTINUE
  110 CONTINUE
      RETURN
	END

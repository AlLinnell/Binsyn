      SUBROUTINE SPHERA(I,ITH,K)
C     TEST SRT TO REPLACE ROCHE MODEL GEOMETRIC PARAMETERS BY
C     SPHERICAL MODEL VALUES
C     FOLLOWING ARRAYS HAVE BEEN SET AND NEED NO CHANGE
C        CSP,SNP,SNT,ALM,AMU,ANU,VL,OMC
      INCLUDE 'COMA.FOR'
      G(I,ITH,K)=-1.D0/VR(I,ITH,K)/VR(I,ITH,K)
      POPL(I,ITH,K)=G(I,ITH,K)
      POPR(I,ITH,K)=G(I,ITH,K)
      POPN(I,ITH,K)=0.D0
      CSB(I,ITH,K)=1.D0
      ZL(I,ITH,K)=ALM(I,ITH,K)/G(I,ITH,K)*POPR(I,ITH,K)
      ZM(I,ITH,K)=AMU(I,ITH,K)/G(I,ITH,K)*POPR(I,ITH,K)
      ZN(I,ITH,K)=ANU(I,ITH,K)/G(I,ITH,K)*POPR(I,ITH,K)      
      RETURN
      END 

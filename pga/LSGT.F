      SUBROUTINE LSGT(CSI,SNI,CM,VL0,VM0,VN0,SNP,CSP,TARY) 
C     SRT TO CALCULATE THE DIRECTION COSINES OF THE LINE OF SIGHT,GIVEN  
C     THE ORBITAL INCLINATION(VI) AND THE LONGITUDE ON THE STAR OF THE   
C     CENTRAL MERIDIAN(CM) WHICH INCLUDES THE LINE OF SIGHT
C     THE SRT ALSO CALCULATES THE TRANSFORMATION MATRIX TO PROJECT X,Y,Z 
C     COODS.ON THE PLANE OF THE SKY
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	PARAMETER (MTR    =    3)
      DIMENSION TARY(MTR,MTR)      
      VN0=CSI    
      SNP=DSIN(CM)
      CSP=DCOS(CM)
      VL0=CSP*SNI
      VM0=SNP*SNI
      TARY(3,1)=VL0     
      TARY(3,2)=VM0     
      TARY(3,3)=VN0     
      TARY(2,3)=0.D0
      TARY(1,1)=CSP*CSI 
      TARY(2,1)=SNP     
      TARY(1,2)=SNP*CSI 
      TARY(2,2)=-CSP    
      TARY(1,3)=-SNI    
    2 RETURN     
      END 

      SUBROUTINE IVPT(I,Q1,VM1,R1,OML11,OML21)      
C     SRT TO CALCULATE THE POTENTIAL,IN CGS UNITS,CORRESPONDING TO A     
C     PARTICULAR KOPAL POTENTIAL,OMC  
C     THIS IS THE INVERSE OF THE ALGORITHM IN SRT CVPT     
      INCLUDE 'COMA.FOR'
      DATA GC/6.6704D-8/ 
      A=GC*VM1/R1
      B=Q1*Q1/2.D0/(1.D0+Q1)
      OML11=A*(OMC(I,1)+B)
      OML21=A*(OMC(I,2)+B)
      RETURN     
      END 

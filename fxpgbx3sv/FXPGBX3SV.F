       PROGRAM FXPGBX3SV
C      Program to produce file PGBX3SV.DAT with KCTRL set to 2
      CHARACTER * 36 FL1
      DIMENSION FL1(2),VLPSI(150000)
      DATA KCTRL2/2/
      DATA IN1/1/
      DATA NOUT/2/
  100 FORMAT (A36)
  106 FORMAT (2I5)
  108 FORMAT (5F16.13)
      OPEN (UNIT=4,     FILE='FXPGBX3SV.ILS', STATUS='OLD')
      READ (4, 100)     (FL1(J),J=1,2)
      OPEN (UNIT=IN1,   FILE=FL1(1), STATUS='OLD')         
      OPEN (UNIT=NOUT,   FILE=FL1(2), STATUS='OLD') 
      READ (IN1,106) KCTRL,NPSI
      READ (IN1,108) (VLPSI(K),K=1,NPSI)
      READ (IN1,108) VI 
      WRITE (NOUT,106) KCTRL2,NPSI
      WRITE (NOUT,108) (VLPSI(K),K=1,NPSI)
      WRITE (NOUT,108) VI
      CLOSE (IN1, STATUS='KEEP')
      CLOSE (NOUT, STATUS='KEEP')       
       END PROGRAM FXPGBX3SV

      SUBROUTINE TDTPM2(J,K,L,GRV,TMP,WL,UV) 
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INCLUDE 'COMD.FOR'
      CALL LNTER(RTMP(K),U2R(J,K,L),RTMP(K+1),U2R(J,K+1,L),TMP,U1)
      CALL LNTER(RTMP(K),U2R(J+1,K,L),RTMP(K+1),U2R(J+1,K+1,L),TMP,U2)
      CALL LNTER(RTMP(K),U2R(J,K,L+1),RTMP(K+1),U2R(J,K+1,L+1),TMP,U3)
      CALL LNTER(RTMP(K),U2R(J+1,K,L+1),RTMP(K+1),U2R(J+1,K+1,L+1),TMP,
     $U4) 
      CALL LNTER(RWL(L),U2,RWL(L+1),U4,WL,U6)
      CALL LNTER(RWL(L),U1,RWL(L+1),U3,WL,U5)
      CALL LNTER(RGRAV(J),U5,RGRAV(J+1),U6,GRV,UV)  
      RETURN     
      END 

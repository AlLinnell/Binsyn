      SUBROUTINE PDFB(AR1,AR2,AR3,AR4,JL,KL,NDF,NOUT)    
C     VERSION OF 8/11/83
C     SRT TO PRINT A HORIZONTAL DIFFERENCE TABLE    
C     AR1 IS THE TABLE OF ABSCISSA VALUES    
C     AR2 IS THE TABLE OF ORDINATE VALUES    
C     AR3 IS THE TABLE OF DIFFERENCES 
C     JL IS THE LENGTH OF AR1 AND AR2 
C     KL IS THE LENGTH OF AR3  
C     NDF IS THE NUMBER OF DIFFERENCES TO BE PRINTED
C     AR4 IS A LOCAL ARRAY,OF LENGTH NDF     
C     NOTE THAT AR4 MUST BE DECLARED IN THE CALLING PROGRAM
C     SEE THE PROGRAM LISTING FOR INFO.ON ADDRESSING IN AR3
C     THIS SRT IS NORMALLY USED AFTER SRT DFB
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION AR1(JL),AR2(JL),AR3(KL),AR4(NDF)    
    1 FORMAT(1X,5D14.8)
    2 FORMAT('    ABSCISSA      ORDINATE      DIFF.1        DIFF.2
     $  DIFF.3')
      WRITE (NOUT,2)
      KJ=0
      DO 3 J=1,JL
      IF (KJ.GT.0) GO TO 6     
      WRITE ( NOUT,1) AR1(J),AR2(J)      
      GO TO 4    
    6 DO 5 K=1,KJ
      KV=(K-1)*JL+J     
      AR4(K)=AR3(KV)    
    5 CONTINUE   
      WRITE ( NOUT,1) AR1(J),AR2(J),(AR4(K),K=1,KJ)    
    4 IF (KJ.GE.NDF) GO TO 3   
      KJ=KJ+1    
    3 CONTINUE   
      RETURN     
      END 

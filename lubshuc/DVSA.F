      SUBROUTINE DVSA(X,Y,Z,VLAMBDA1,VM1,B1,B2,UX,UY,YP,DYDT)
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	DIMENSION YP(4),DYDT(4)
	YP(1)=X
	YP(2)=Y
	UX=VLAMBDA1*X*(1.D0+B1*X)
	UY=VLAMBDA1*Y*(1.D0+B2*Y/VM1**2)
	DUXDT=VLAMBDA1**2*X*(1.D0+3.D0*B1*X)
	DUYDT=VLAMBDA1**2*Y*(1.D0+3.D0*B2*Y/VM1**2)
	YP(3)=UX
	YP(4)=UY
	DYDT(1)=UX
	DYDT(2)=UY
	DYDT(3)=DUXDT
	DYDT(4)=DUYDT
	RETURN
	END
	     
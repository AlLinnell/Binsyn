      SUBROUTINE DERIVS(X,Y,Z,VMU,VMOMU,UX,UY,YP,DYDT)
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	DIMENSION YP(4),DYDT(4)
	YP(1)=X
	YP(2)=Y
	YP(3)=UX
	YP(4)=UY
	DYDT(1)=UX
	DYDT(2)=UY
	CALL PPCMPX(X,Y,Z,VMU,VMOMU,PPOTX)
	CALL PPCMPY(X,Y,Z,VMU,VMOMU,PPOTY)
	CALL SDUXDT(PPOTX,UY,DYDT(3))
	CALL SDUYDT(PPOTY,UX,DYDT(4))
	RETURN
	END
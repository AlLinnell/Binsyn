      FUNCTION RTBISB(FUNC,VMU,VMOMU,PTRF,X1,X2,XACC,IER)
	IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      PARAMETER (JMAX=40)
	IER=0
      CALL FUNC(X2,0.D0,0.D0,VMU,VMOMU,PTRF,FMID)
      CALL FUNC(X1,0.D0,0.D0,VMU,VMOMU,PTRF,F)
      IF(F*FMID.GE.0.D0) IER=1
	IF (IER.NE.0) RETURN
      IF(F.LT.0.D0)THEN
        RTBISB=X1
        DX=X2-X1
      ELSE
        RTBISB=X2
        DX=X1-X2
      ENDIF
      DO 11 J=1,JMAX
        DX=DX*.5D0
        XMID=RTBISB+DX
        CALL FUNC(XMID,0.D0,0.D0,VMU,VMOMU,PTRF,FMID)
        IF(FMID.LE.0.D0)RTBISB=XMID
        IF(DABS(DX).LT.XACC .OR. FMID.EQ.0.D0) RETURN
11    CONTINUE
      IER=2
      END

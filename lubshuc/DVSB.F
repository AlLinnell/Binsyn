      SUBROUTINE DVSB(XV,Y,Z,VMU,VMOMU,UX,UY,VLI,AX,AY,AS,
     $ALPHAV,BETAV,GAMMAV,CHIV,VNUV,RHO20V,RDCV,YP,DYDT)
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
	DIMENSION YP(13),DYDT(13)
	DATA TWPI/6.2831853071795864769D0/
	YP(1)=XV
	YP(2)=Y
	YP(3)=UX
	YP(4)=UY
	UV=DSQRT(UX**2+UY**2)
	YP(5)=UV
	YP(6)=ALPHAV
	YP(7)=BETAV
	YP(8)=GAMMAV
	YP(9)=VLI
	YP(10)=CHIV
	YP(11)=VNUV
	RHO20V=DSQRT(YP(8)/TWPI)/YP(5)
	YP(12)=RHO20V
	YP(13)=RDCV
	DYDT(1)=UX
	DYDT(2)=UY
	CALL PPCMPX(XV,Y,Z,VMU,VMOMU,PPOTX)
	CALL PPCMPY(XV,Y,Z,VMU,VMOMU,PPOTY)
	CALL SDUXDT(PPOTX,UY,DYDT(3))
	CALL SDUYDT(PPOTY,UX,DYDT(4))
	AX=DYDT(3)
	AY=DYDT(4)
	AS=DSQRT(AX**2+AY**2)
	DYDT(5)=AS
	DIDTV=DCOS(VLI)**2*(UX*AY-UY*AX)/UX**2
	DYDT(9)=DIDTV
	CALL P2CMPNPS(XV,Y,Z,VMU,VMOMU,VLI,DYDT(9),UX,UY,UV,AX,AY,AS,
     $PP2PNPS)
	CALL P2CMPN(XV,Y,Z,VMU,VMOMU,VLI,DYDT(9),UX,UY,PP2PN)
	CALL PP2CMPX(XV,Y,Z,VMU,VMOMU,PP2CMTX)
	CALL PP2CMPY(XV,Y,Z,VMU,VMOMU,PP2CMTY)
	CALL P2CMPXPY(XV,Y,Z,VMU,VMOMU,P2CMTXTY)
	CALL PP2CMPZ(XV,Y,Z,VMU,VMOMU,PP2CMTZ)
	DYDT(6)=-(DYDT(5)/YP(5)*ALPHAV-(YP(5)/YP(13)+YP(6))*YP(7)-PP2PNPS+
     $2.D0*YP(7))
	DYDT(7)=-PP2PN+YP(8)-2.D0*YP(6)-YP(7)**2-(YP(5)/YP(13))**2+
     $2.D0*YP(5)*YP(6)/YP(13)
	DYDT(8)=-2.D0*YP(7)*YP(8)
	DYDT(9)=DCOS(YP(9))**2*(YP(3)*DYDT(4)-YP(4)*DYDT(3))/YP(3)**2
	DYDT(10)=-2.D0*YP(11)*YP(10)
	DYDT(11)=-YP(11)**2+YP(10)-PP2CMTZ
	DYDT(12)=-YP(12)*(YP(7)+DYDT(5)/YP(5))
	C1=YP(4)*DYDT(3)-YP(3)*DYDT(4)
	C2=2.D0*(YP(3)*DYDT(3)+YP(4)*DYDT(4))+YP(3)*YP(4)*(PP2CMTY-
     $PP2CMTX)+P2CMTXTY*(YP(3)**2-YP(4)**2)
	DYDT(13)=YP(5)/C1**2*(3.D0*C1*(YP(3)*DYDT(3)+YP(4)*DYDT(4))-
     $YP(5)**2*C2)
	DO J=6,13	         !!!
	YP(J)=1.D0			 !!!
	DYDT(J)=1.D0		 !!!
	END DO				 !!!
	RETURN
	END
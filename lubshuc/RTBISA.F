      FUNCTION RTBISA(FUNC,ISTAR,ICON,VMU,VMOMU,PHI,PTRF,
     $X1,X2,XACC,IER)
	IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      PARAMETER (JMAX=40)
	IER=0
      CALL FUNC(ISTAR,ICON,VMU,VMOMU,PHI,PTRF,FMID,X2)
      CALL FUNC(ISTAR,ICON,VMU,VMOMU,PHI,PTRF,F,X1)
      IF(F*FMID.GE.0.D0) IER=1
	IF (IER.NE.0) RETURN
      IF(F.LT.0.D0)THEN
        RTBISA=X1
        DX=X2-X1
      ELSE
        RTBISA=X2
        DX=X1-X2
      ENDIF
      DO 11 J=1,JMAX
        DX=DX*.5D0
        XMID=RTBISA+DX
        CALL FUNC(ISTAR,ICON,VMU,VMOMU,PHI,PTRF,FMID,XMID)
        IF(FMID.LE.0.D0)RTBISA=XMID
        IF(DABS(DX).LT.XACC .OR. FMID.EQ.0.D0) RETURN
11    CONTINUE
      IER=2
      END
